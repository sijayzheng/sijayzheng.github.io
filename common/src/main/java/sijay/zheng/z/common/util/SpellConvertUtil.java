    private static String getSecondLevelCNSpell(int qwm) {
        if (secondLevelCN().containsKey(String.valueOf(qwm))) {
            return String.valueOf(secondLevelCN().get(String.valueOf(qwm)));
        } else {
            return "";
        }
    }

    private static String getSecondLevelCNSpellFromFile(int qwm) {
        String py;
        py = "";
        Properties prop = new Properties();
        FileInputStream fis = null;
        try {
            fis = new FileInputStream("secCNSpell.properties");
            prop.load(fis);
            py = prop.getProperty(String.valueOf(qwm), "");
        } catch (IOException ex) {
            System.out.println("Cannot load the spell properties file.");
            ex.printStackTrace();
        } finally {
            try {
                fis.close();
            } catch (IOException ex1) {
                ex1.printStackTrace();
            }
        }
        return py;
    }

